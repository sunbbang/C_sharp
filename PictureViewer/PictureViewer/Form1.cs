        {

        }

        private void checkboxStretch_CheckedChanged(object sender, EventArgs e)
        {
            if (checkboxStretch.Checked)
                pictureBox1.SizeMode = PictureBoxSizeMode.StretchImage;
            else
                pictureBox1.SizeMode = PictureBoxSizeMode.Normal;
        }
